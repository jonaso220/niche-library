import type { Perfume } from '@/types/perfume'

function p(
  id: string, name: string, brand: string, concentration: 'EDT' | 'EDP' | 'Extrait' | 'Parfum' | 'EDC' | 'Other',
  gender: 'masculino' | 'femenino' | 'unisex', rating: number, longevity: number, sillage: number,
  top: string[], middle: string[], base: string[],
  accords: [string, number][],
  seasons: [number, number, number, number], // spring, summer, fall, winter
  occasions: [number, number, number, number, number], // casual, professional, nightOut, date, special
  year?: number, imageUrl?: string
): Perfume {
  return {
    id, name, brand, year, gender, concentration, rating, longevity, sillage,
    notes: {
      top: top.map(n => ({ name: n })),
      middle: middle.map(n => ({ name: n })),
      base: base.map(n => ({ name: n })),
    },
    accords: accords.map(([name, percentage]) => ({ name, percentage })),
    seasonScores: [
      { season: 'spring', score: seasons[0] },
      { season: 'summer', score: seasons[1] },
      { season: 'fall', score: seasons[2] },
      { season: 'winter', score: seasons[3] },
    ],
    occasionScores: [
      { occasion: 'casual', score: occasions[0] },
      { occasion: 'professional', score: occasions[1] },
      { occasion: 'nightOut', score: occasions[2] },
      { occasion: 'date', score: occasions[3] },
      { occasion: 'special', score: occasions[4] },
    ],
    imageUrl,
    dataSource: 'seed',
  }
}

